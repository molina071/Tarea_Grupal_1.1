package com.example.tarea_grupal_11.Classes;

public class Suma {

    private double valorNum1;
    private double valorNum2;

    public Suma(double valorNum1, double valorNum2) {
        this.valorNum1 = this.valorNum1;
        this.valorNum2 = this.valorNum2;
    }

    public double suma(){
        return valorNum1 + valorNum2;
    }


}
